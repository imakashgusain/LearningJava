package switchevolution;

public class Java7SwitchExample {

    public static void main(String[] args){
        int number = 1;
        switch (number) {
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("Unknown");
        }
    }
}
